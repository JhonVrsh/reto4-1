        String repetirPassword = txtConfirmarPassword.getText();
        // validamos si las contraseñas son iguales 
        if(nuevaPassword.equals(repetirPassword)){
            JOptionPane.showMessageDialog(this,"Cambio exitoso","Actualización de la contraseña",JOptionPane.INFORMATION_MESSAGE);
            
            
        }else{
            JOptionPane.showMessageDialog(this,"Las contraseñas no coinciden","Actualización de la contraseña",JOptionPane.ERROR_MESSAGE);
            System.out.println("Contraseña nueva: ");

        }
